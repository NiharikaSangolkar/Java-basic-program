// Write a Java Program to Compute the Sum of Array Elements.

class sumofarray{
    public static void main(String[] args) {
       // System.out.println("Try programiz.pro");
       int arr[]={2,4,9,6,7};
       int sum=0;
       for(int i=0;i<arr.length;i++)
       {
           
           sum+=arr[i];
       }
       System.out.println("sum of arrays:"+sum);
    }
}
